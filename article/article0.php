				<h2>A guide to making your very own candle at home </h2><br><br>
                <h3>Introduction </h3>
				<p>
                    We at Lumina believe that candles have a unique way of 
                    bringing warmth and ambiance to any space. We’ve been 
                    creating handcrafted candles for almost a decade and 
                    our inspiration has always been the beauty of our local 
                    sceneries, Vancouver and also special events of the year.
                     And now as we celebrate our anniversary, we would like 
                     to share the joy of candle-making with you. In this step-by-step guide, 
                    you’ll learn how to make your only lumina-inspired
                     candles right in the comfort of your home.
				</p> <br> <br>

				<h3>Before we start, you will need the following material:</h3>

				<p>
                    -	Wax (soy, beeswax, paraffin or any wax preference you want to use)<br>
                    -	fragrance oils (for scented candles but not required) also available on our website <br>
                    -	wax colour dyes (if preferred coloured)<br>
                    -	Boiler or microwave (for melting wax)<br>
                    -	Thermometer<br>
                    -	Candle moulder or container<br>
                    -	Stirring utensils<br>
                    -	Wicks holder<br>
                    
				</p>

                <img src="./img/Blog_Images/Materials.jpeg" alt="photo">

				<h3>Step #1: Preparation</h3>
				<p>
                    -	Set up a clean, organized workspace. <br>
                    -	Use newspaper, or tablecloth to catch any spills during the production <br>
                    -	Wear gloves and googles (Recommended)
                    
				</p>
                <img src="./img/Blog_Images/Step1.jpeg" alt="photo1">
				<h3>Step #2: Wicks Preparation </h3>
				<p>
                    -	Cut the wick to the desired length, leaving a 
                    little extra to ensure it reaches the bottom of the container. <br>

                    -	Attach the one end of the wick at the bottom of the container
                    and apply adhesiveto ensure its place <br>

                    -	Use a stick or anything that can hold the other 
                    end of the wick on top of thecontainer to secure the placement 

				</p>
                <img src="./img/Blog_Images/Step2.jpeg" alt="photo2">
				<h3>Step #3: Melt The Wax </h3>
				<p>
                    -	Melt your chosen wax using a boiler or microwave. <br>
                    -	Ensure to follow the manufacturer’s instructions 
                    regarding the duration of melting the wax as it varies from wax <br>
                    -	Use a thermometer to monitor wax temperature and prevent it from overheating. 
                    Ideally, the temperature must be 130-300 F. Do not melt more than the standard as it can result in  <br>
                    -	Safety first: do not touch the melted wax as the temperature could potentially burn your skin
				</p>
                <img src="./img/Blog_Images/Step3.jpeg" alt="photo3">
				<h3>Step #4: Add Fragrance and colours</h3>
				<p>
                    -	If you are not making a scented candle, you may skip this step <br>
                    -	With the chosen fragrance, add a few drops to 
                    the melted wax and mix well. You can also add dye to achieve the consistency you are aiming for<br> 
                    -	Note: do not add too much fragrance oil to the melted wax as this might result in a strong smell. 

				</p>
                <img src="./img/Blog_Images/Step4.jpeg" alt="photo3">
				<h3>Step #6: Leave the candle</h3>
				<p>
                    -	Allow the candle to cool and solidify for a couple of hours. Be patient <br>
                    -	During this process, do not cover the candle with a lid 
                    
				</p>
                <img src="./img/Blog_Images/Step5.jpeg" alt="photo4">

                <h3>Step #5: Pour into the container or molder</h3>
				<p>- Carefully pour the melted wax into the container. ensure the wick remains center.
				</p>
                <img src="./img/Blog_Images/Step6.jpeg" alt="photo5">

                <h3>Step #7: Trim The Wick</h3>
				<p>- Once the candle has hardened and cooled., trim the wicks to about an inch above the surface of the candle <br>
				</p>
                <img src="./img/Blog_Images/Step7.jpeg" alt="photo6">

                <h3>Step #8: Enjoy your homemade lumina style candle</h3>
				<p>
                    - Now to the good part, you can start to enjoy your 
                    lumina-inspired candles. light up and fill your space
                    with the comforting glow and aroma that you love. - 
                    If you are not happy with the scent, 
                    you can always restart and try our scented fragrance
                     oils available on our website. We assure you the 
                     best scent ever to exist 
				</p>
                <img src="./img/Blog_Images/Step8.jpeg" alt="photo7">


                <h3>Conclusion</h3>
				<p>
                    Crafting your own candles is always a rewarding and 
                    creative experience which is why Lumina has been 
                    crafting for a decade.  We hope that this quick guide has 
                    inspired you to embark on your own candle-making 
                    journey. Whether you choose to create candles
                     for personal use or for occasion, the artistry of
                      candle-making can truly be magical and fulfilling
                      . We hope you enjoyed this quick easy guide!   
				</p>
				<h4>
                    Stay tuned for more tips, candle-making ideas, and lumina updates! Till next time!
				</h4>



